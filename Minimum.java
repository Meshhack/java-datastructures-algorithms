public class Minimum {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;
        int minValue = min(a, b);
        System.out.println("The minimum value is: " + minValue);
    }
    public static int min(int a, int b) {
         a = 4;
         b = 5;
        if (a < b) {
            return a; 
        }       else {
            return b;
    }


}}
